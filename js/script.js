// –í–∑–µ–º–∞–º–µ —Ä–µ—Ñ–µ—Ä–µ–Ω—Ü–∏—è –∫—ä–º –±—É—Ç–æ–Ω–∞ –∑–∞ —Å–º—è–Ω–∞ –Ω–∞ —Ç–µ–º–∞
const themeButton = document.getElementById('toggle-theme');

// –î–æ–±–∞–≤—è–º–µ —Å–ª—É—à–∞—Ç–µ–ª –∑–∞ —Å—ä–±–∏—Ç–∏–µ –ø—Ä–∏ –∫–ª–∏–∫ –≤—ä—Ä—Ö—É –±—É—Ç–æ–Ω–∞
themeButton.addEventListener('click', () => {
  // –ü—Ä–µ–≤–∫–ª—é—á–≤–∞–º–µ –∫–ª–∞—Å–∞ "dark-mode" –∫—ä–º <body>, –∑–∞ –¥–∞ —Å–º–µ–Ω–∏–º —Ç–µ–º–∞—Ç–∞
  document.body.classList.toggle('dark-mode');
  
  // –ü—Ä–æ–º–µ–Ω—è–º–µ —Ç–µ–∫—Å—Ç–∞ –Ω–∞ –±—É—Ç–æ–Ω–∞ –≤ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç –æ—Ç —Ç–µ–∫—É—â–∞—Ç–∞ —Ç–µ–º–∞
  themeButton.textContent = document.body.classList.contains('dark-mode') ? 'üåô' : 'üåû';
});

// –í–∑–µ–º–∞–º–µ —Ä–µ—Ñ–µ—Ä–µ–Ω—Ü–∏—è –∫—ä–º –∫–æ–Ω—Ç–∞–∫—Ç–Ω–∞—Ç–∞ —Ñ–æ—Ä–º–∞ –∏ –¥–æ–±–∞–≤—è–º–µ —Å–ª—É—à–∞—Ç–µ–ª –∑–∞ —Å—ä–±–∏—Ç–∏–µ "submit"
document.getElementById('contact-form').addEventListener('submit', function (e) {
  // –ü—Ä–µ–¥–æ—Ç–≤—Ä–∞—Ç—è–≤–∞–º–µ –∏–∑–ø—Ä–∞—â–∞–Ω–µ—Ç–æ –Ω–∞ —Ñ–æ—Ä–º–∞—Ç–∞, –∑–∞ –¥–∞ –∏–∑–≤—ä—Ä—à–∏–º –≤–∞–ª–∏–¥–∞—Ü–∏—è
  e.preventDefault();

  // –í–∑–µ–º–∞–º–µ —Å—Ç–æ–π–Ω–æ—Å—Ç–∏—Ç–µ –æ—Ç –ø–æ–ª–µ—Ç–∞—Ç–∞ –Ω–∞ —Ñ–æ—Ä–º–∞—Ç–∞
  const name = document.getElementById('name').value.trim();
  const email = document.getElementById('contact-email').value.trim();
  const subject = document.getElementById('subject').value.trim();
  const message = document.getElementById('message').value.trim();

  // –ü—Ä–æ–º–µ–Ω–ª–∏–≤–∏ –∑–∞ —Å–ª–µ–¥–µ–Ω–µ –Ω–∞ –≤–∞–ª–∏–¥–Ω–æ—Å—Ç –∏ –≥—Ä–µ—à–∫–∏
  let isValid = true;
  let errorMessage = "";

  // –ü—Ä–æ–≤–µ—Ä–∫–∞ –¥–∞–ª–∏ –ø–æ–ª–µ—Ç–æ "–ò–º–µ" –µ –ø–æ–ø—ä–ª–Ω–µ–Ω–æ
  if (name === "") {
    isValid = false;
    errorMessage += "–ú–æ–ª—è, –≤—ä–≤–µ–¥–µ—Ç–µ –≤–∞—à–µ—Ç–æ –∏–º–µ.\n";
  }

  // –ü—Ä–æ–≤–µ—Ä–∫–∞ –¥–∞–ª–∏ –ø–æ–ª–µ—Ç–æ "–ò–º–µ–π–ª" –µ –ø–æ–ø—ä–ª–Ω–µ–Ω–æ –∏ –¥–∞–ª–∏ —Å—ä–¥—ä—Ä–∂–∞ –≤–∞–ª–∏–¥–µ–Ω –∞–¥—Ä–µ—Å
  if (email === "") {
    isValid = false;
    errorMessage += "–ú–æ–ª—è, –≤—ä–≤–µ–¥–µ—Ç–µ –≤–∞—à–∏—è –∏–º–µ–π–ª.\n";
  } else if (!validateEmail(email)) {
    isValid = false;
    errorMessage += "–ú–æ–ª—è, –≤—ä–≤–µ–¥–µ—Ç–µ –≤–∞–ª–∏–¥–µ–Ω –∏–º–µ–π–ª –∞–¥—Ä–µ—Å.\n";
  }

  // –ü—Ä–æ–≤–µ—Ä–∫–∞ –¥–∞–ª–∏ –ø–æ–ª–µ—Ç–æ "–¢–µ–º–∞" –µ –ø–æ–ø—ä–ª–Ω–µ–Ω–æ
  if (subject === "") {
    isValid = false;
    errorMessage += "–ú–æ–ª—è, –≤—ä–≤–µ–¥–µ—Ç–µ —Ç–µ–º–∞ –Ω–∞ —Å—ä–æ–±—â–µ–Ω–∏–µ—Ç–æ.\n";
  }

  // –ü—Ä–æ–≤–µ—Ä–∫–∞ –¥–∞–ª–∏ –ø–æ–ª–µ—Ç–æ "–°—ä–æ–±—â–µ–Ω–∏–µ" –µ –ø–æ–ø—ä–ª–Ω–µ–Ω–æ
  if (message === "") {
    isValid = false;
    errorMessage += "–ú–æ–ª—è, –≤—ä–≤–µ–¥–µ—Ç–µ —Å—ä–æ–±—â–µ–Ω–∏–µ.\n";
  }

  // –ê–∫–æ –∏–º–∞ –≥—Ä–µ—à–∫–∏, –ø–æ–∫–∞–∑–≤–∞–º–µ —Å—ä–æ–±—â–µ–Ω–∏–µ —Å –≤—Å–∏—á–∫–∏ –≥—Ä–µ—à–∫–∏
  if (!isValid) {
    alert(errorMessage);
  } else {
    // –ê–∫–æ –Ω—è–º–∞ –≥—Ä–µ—à–∫–∏, –ø–æ–∫–∞–∑–≤–∞–º–µ —Å—ä–æ–±—â–µ–Ω–∏–µ –∑–∞ —É—Å–ø–µ—à–Ω–æ –∏–∑–ø—Ä–∞—â–∞–Ω–µ
    alert("–§–æ—Ä–º–∞—Ç–∞ –µ –∏–∑–ø—Ä–∞—Ç–µ–Ω–∞ —É—Å–ø–µ—à–Ω–æ!");
  }
});

// –§—É–Ω–∫—Ü–∏—è –∑–∞ –ø—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞ –≤–∞–ª–∏–¥–Ω–æ—Å—Ç—Ç–∞ –Ω–∞ –∏–º–µ–π–ª –∞–¥—Ä–µ—Å
function validateEmail(email) {
  // –†–µ–≥—É–ª—è—Ä–µ–Ω –∏–∑—Ä–∞–∑ –∑–∞ –≤–∞–ª–∏–¥–∏—Ä–∞–Ω–µ –Ω–∞ —Å—Ç–∞–Ω–¥–∞—Ä—Ç–µ–Ω —Ñ–æ—Ä–º–∞—Ç –∑–∞ –∏–º–µ–π–ª
  const regex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
  return regex.test(email); // –í—Ä—ä—â–∞ true, –∞–∫–æ –∏–º–µ–π–ª—ä—Ç –µ –≤–∞–ª–∏–¥–µ–Ω
}
